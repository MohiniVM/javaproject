package package_if;

public class Switch {

	public static void main(String[] args) {
	
		int digit;
       switch (digit=20)
       {
       case 10: System.out.println("addition");
       break;
       case 15:System.out.println("subtraction");
       break;
       case 20: System.out.println(" multiplication");
       break;
       default: System.out.println("division");
       }
	}

}
  