package loop;

public class While_loop {

	public static void main(String[] args) {
		
		int i = 10;
		while(i>5)
		{
			System.out.println("while loop is executed" +i);
			i--;
		}
		int j = 4;
		do
		{
			System.out.println("do while loop is executed" +j);
			j++;
		}
		while(j<6);
		
		
		
	}

}
